jQuery(function($) {
// -------------------------------------------------------------------


// ---------------------------------------------------------------------------------
// ----- GLOBAL --------------------------------------------------------------------
// ---------------------------------------------------------------------------------


// ---------------------------------------------------------------------------------
// ----- MOBILE NAVIGATION ---------------------------------------------------------
// ---------------------------------------------------------------------------------
// mobile navigation
  $(document).delegate(
    ".mobile-nav-activate, .mobile-nav-close",
    "click",
    function(e) {
      console.log("nav-clicked");
      $(".mobile-nav").toggleClass("active");
    }
  );


// ---------------------------------------------------------------------------------
// ----- NINJA FORM SUBMIT ---------------------------------------------------------
// ---------------------------------------------------------------------------------
$(document).on('nfFormReady', function() {
	$('.footer-wrap .nf-form-cont .submit-container input[type=button]').hide();

	$('.footer-wrap .nf-form-cont input:not([type=button])').keypress(function(e) {
		if (e.keyCode === 13) {
			$('.nf-form-cont .submit-container input[type=button]').click();
		}
	});
});







// -------------------------------------------------------------------

});
